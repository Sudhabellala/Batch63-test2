package javaOperators;

public class Relational {
	static int a=10;
	static int b=20;

	public static void main(String[] args) {
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a<b);
		System.out.println(a<=b);
		
	}

}
