package oops;

public class PersonData {
	private String name;
	private int age;
	private String email;
	
	void setName(String name) {
		this.name=name;
	}
	
	String getName() {
		return name;
	}
	
	void setAge(int age) {
		this.age=age;
	}
	
	int getAge() {
		return age;
	}
	
	void setEmail(String email) {
		this.email=email;
	}
	String getEmail() {
		return email;
	}

}
